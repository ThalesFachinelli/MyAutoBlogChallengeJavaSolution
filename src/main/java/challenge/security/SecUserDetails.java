package challenge.security;

import java.util.Collection;
import java.util.HashSet;
import java.util.Set;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import challenge.model.User;

public class SecUserDetails implements UserDetails {

	private static final long serialVersionUID = 4727576833694691816L;
	private static Set<GrantedAuthority> defaultGrantedAuthorities;

	static {
		defaultGrantedAuthorities = new HashSet<>();
		defaultGrantedAuthorities.add(new SimpleGrantedAuthority("USER"));
	}

	private User user;

	public SecUserDetails(User user) {
		super();
		this.user = user;
	}

	public User getUser() {
		return user;
	}

	@Override
	public Collection<? extends GrantedAuthority> getAuthorities() {
		return defaultGrantedAuthorities;
	}

	@Override
	public String getPassword() {
		return user.getPassword();
	}

	@Override
	public String getUsername() {
		return user.getEmail();
	}

	@Override
	public boolean isAccountNonExpired() {
		return true;
	}

	@Override
	public boolean isAccountNonLocked() {
		return true;
	}

	@Override
	public boolean isCredentialsNonExpired() {
		return true;
	}

	@Override
	public boolean isEnabled() {
		return user.isEnabled();
	}

}
